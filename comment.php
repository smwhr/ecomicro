  <center>
  <b><FONT size=4>FAQ - Comment faire ?</FONT></b>
  </center>

  <br>
  <br>

  <b>Comment revenir au détail de mon personnage ?</b><br>
  Cliquez sur son portait en haut à droite.
  <br>

  <br>

  <b>Comment modifier mon mot de passe ?</b><br>
  Au dessus du portait de votre avatar, cliquez sur 'Modifier' (en vert), saisissez dans la zone 'mot de passe' votre nouveau mot de passe...
  <br>
  Si vous désirez juste modifier un autre champ de votre profil, laissez le champs 'mot de passe' vide, celui ci ne sera alors pas modifié.
  <br>

  <br>

  <b>Comment modifier la description ou la photo d'une de mes possessions ?</b><br>
  Vous ne pouvez pas !
  <br>
  <br>
  Pour obtenir un produit à l'allure particulière demandez une autre image au vendeur avant de l'acheter lorsque vous négocitez le prix.
  Le vendeur peut modifier les caractéristiques des produits qu'il met en vente, il pourra donc mettre une image et une description personnalisée.
  Ensuite vous effectuez l'opération de vente (proposition + validation) et le vendeur pourra s'il le désire remettre les données par défaut.
  <br>
  <br>

  <b>Comment valider un message (achat/vente, ouverture de relation, taux de change) ?</b><br>
  Losqu'une personne effectue un achat, elle n'achète pas directement le produit mais propose au vendeur ces conditions d'achat.
  Il en va de même pour les autres types d'opération.
  Le vendeur, ou le responsable économique s'il ne s'agit pas d'un achat/vente, doit alors valider la proposition pour que l'opération soit réellement effectuée.
  <br>
  <br>
  En haut à droite cliquez sur 'Messagerie', puis cliquez sur le message (colonne 'demande').
  La proposition apparait en gros et il vous est possible soit d'annuler, si vous êtes la personne qui a effectué la proposition (par exemple l'achat), soit d'accepter ou de refuser la proposition (pour le vendeur par exemple).
  <br>
  <br>
  C'est au moment de la validation (acceptation) que la recherche du taux de change et de la taxe sont effectués, par conséquent ces derniers peuvent être différents du moment où l'acheteur a effectué sa proposition.
  <br>
  <br>

  <b>Comment mettre en vente un nouveau produit ?</b><br>
  Vous ne pouvez créer un nouveau produit que si vous gérez une entité économique (entreprise, province, organisme).
  <br>
  <br>
  Allez sur le détail d'une des entreprises que vous dirigez, puis sur le bouton 'Catalogue de vente'.Il vous reste à cliquer sur 'Nouveau produit'.
  Saisissez le nom du produit et choisissez ensuite son type.
  <br>
  Dans le champ 'Image' vous devez saisir l'adresse de l'image (http://www.titi.fr/images/toto.gif) ou '#' par exemple si vous n'en avez pas pour le moment.
  L'image n'est pas contrôlée actuellement, ni copiée sur le serveur. Veillez à ce que l'image soit de taille raisonnable : moins de 50 ko.
  Lors de son affichage elle sera redimensionnée à 200 en largeur et 100 en hauteur, veuillez donc choisir une image rectangulaire conservant plus ou moins ce rapport de 2/1.
  <br>
  Le nombre d'unité et l'unité sont en fonction de ce qui se fait dans votre pays.
  De même, il peut être illogique ou interdit, selon les lois en vigueur dans la nation, qu'un type d'entité mette en vente des produits.
  EcoMicro ne contrôlant pas ces cohérences, renseignez vous auprés de votre reponsable.
  <br>

  <br>
	Pour qu'une possibilité de commerce existe entre plusieurs nations, le mode de détermination de la valeur des produits doit être similaire. 
	C'est pourquoi EcoMicro propose un outil qui détermine cette valeur à partir du type de produit et de son prix (en Euro) approximatif.
  <br>
  <br>

  <b>Comment faire pour modifier ou supprimer un de mes produits en vente ?</b><br>
  Allez sur le détail d'une des entreprises que vous dirigez, puis sur le bouton 'Catalogue de vente'. Cliquez sur le nom du produit dont vous souhaitez modifier les données ou que vous souhaitez supprimer.
  <br>
  <br>

  <b>Comment créer un nouveau compte bancaire ?</b><br>
  Au niveau du détail de votre personnage, cliquez à droite sur l'un de vos compte puis sur 'Nouveau Compte'.
  Choisissez alors le titulaire du compte (s'il s'agit de vous personnellement ou de l'une des entreprises, provinces ou organismes que vous gérez).
  <br>
  Saisissez ensuite le libellé du compte.
  Enfin choisissez la devise.
  <br>
  <br>

  <b>Comment faire pour supprimer un compte bancaire ?</b><br>
  Au niveau du détail de votre personnage, cliquez à droite sur l'un de vos compte puis sur 'Supprimer un compte'.
  <br>

  Choissisez le compte et notez que les fonds seront perdus.
  <br>
  N'ayez crainte, s'il s'agit de l'unique compte de l'entité, ou si le compte d'Etat est utilisé au niveau du change, il ne pourra pas être supprimé.
  <br>
  <br>

  <b>Quelle est la différence entre entreprise, province et organisme ?</b><br>
  Il n'y en a pas vraiment, il s'agit simplement de les regrouper par type. Il existe toutefois des spécificités.
  D'ailleurs le terme 'entreprise' est souvent utilisé pour identifier une entreprise, une province ou un organisme.
  Les termes 'entité' et 'entité économique' correspondent à la même notion. Cela allège le texte, bien que celui ci soit perfectible à bien des égards...
  <br>
  <br>

  Cependant la production d'un certain type d'unité de stock (PA, PE, PP, PDt...) est autorisée en fonction du type d'entreprise.
  Ainsi une entreprise du secteur secondaire pourra effectuer certaines règles de transformation.
  Une entreprise de type ferme ne pourra que produire des PA.
  <br>
  Un organisme ou une province ne peut rien produire et celles ci n'apparaissent pas à la Bourse.
  <br>
  <br>
	Les provinces sont indispensable pour localiser les batiments bien entendu.
  <br>
  <br>

  <b>Comment faire pour voir le détail d'une entreprise, d'une province ou d'une organisation ?</b><br>

  Dans de nombreux écrans de liste par simple clique sur le nom de l'entreprise s'il est vert.
  <br>
  <br>

  <b>Que signifient les 3 nombres au niveau de la capacité mensuelle ?</b><br>
  Le premier indique votre reste à produire pour le mois en cours.
  Le deuxième indique votre capacité de production totale pour le mois actuel.
  Enfin le troisième, entre parenthèse, indique votre capacité de production mensuelle maximum.
  <br>
  <br>
  La capacité de production totale pour le mois en cours est déterminée chaque mois, pour les entreprises du secteur primaire, en fonction de la règle décrite au niveau du système économique.
  Elle fait intervenir le nombre de citoyen actif (s'étant connecté il y a moins de deux mois) et la somme des capacités maximum de toutes les entreprises du secteur primaire ayant un directeur.
  <br>

  La capacité de production totale pour le mois en cours est égale à la capacité maximum pour les autres types d'entreprises.
  <br>
  <br>

  <b>Comment faire pour produire des unités de stock ?</b><br>
  Dans le détail d'une entreprise, cliquez sur le bouton 'Stock/Productions'.
  Les stocks de l'entreprise apparaissent, ainsi qu'une liste des unités qu'elle est en mesure de produire.
  Sélectionnez l'unité que vous souhaitez produire.
  <br>
  Dans le tableau s'affiche les ressources et leur quantité nécessaires, ainsi que les productions annexes (comme les déchets par exemple).
  En indiquant le nombre d'unité que vous souhaitez produire et en cliquant sur le bouton 'Donne', des contrôles sont effectués et les valeurs correspondantes pour les ressources et productions annexes sont affichées.
  <br>
  Il ne vous reste plus qu'à lancer la prodcution. Celle ci est immédiate.
  <br>

  <br>

  <b>Que signifie le choix 'Consommation' au niveau du détail d'une entreprise ?</b><br>
  Ce choix vous permet comme son nom l'indique de consommer des unités.
  C'est nécessaire lorsque votre entreprise perd des marchandises ou les consomme (dans le cadre d'un transport par exemple).
  <br>
  Les unités ainsi consommées sont perdues.
  <br>
  <br>

  <b>Comment affecter un poste de directeur d'une entreprise, ou modifer les données ?</b><br>

  Ce choix est autorisé pour l'actuel directeur et le responsable économique.
  Au niveau du détail de l'entreprise, cliquez sur le lien vert 'Modifier' en haut au centre.
  <br>
  <br>

  <b>Comment faire pour créer une entreprise/organisme/province ?</b><br>
  Seul le responsable peut en créer.
  Pour ce faire il doit se rendre à l'écran 'Entreprises' et cliquer sur le lien vert 'Nouvelle entreprise'.
  <br>
  <br>

  <b>Comment faire si la nouvelle entreprise n'est pas possédée à 100% lors de sa création ?</b><br>

  Vous n'avez pas la possibilité de saisir le détail.
  L'astuce consiste à choisir un propriétaire marlgré tout. Ce dernier pourra alors vendre ces titres à ces associés.
  Il est préférable que le propriétaire choisi soit loyal, l'Etat est donc une bonne option pour éviter que le propriétaire déterminé refuse ensuite de vendre ces titres.
  <br>
  <br>

  <b>Comment supprimer une entreprise ?</b><br>
  Cela n'est pas possible actuellement.
  <br>
  <br>

  <b>Comment ajouter un citoyen ?</b><br>

  Au niveau de l'écran 'Citoyens', cliquez le lien en vert 'Nouveau citoyen'.
  <br>
  <br>

  <b>Comment supprimer un  citoyen ?</b><br>
  Cela n'est pas possible actuellement.
  <br>
  <br>

  <b>A quoi correspond la liste 'Besoins' du détail d'un Etat ?</b><br>

  Il s'agit des besoins de la nation visualisée.
  <br>
  En cliquant sur une unité, le responsable peut affecter ce besoin à une entreprise, une province ou un organisme.
  Celle ci voit alors ces stocks diminués, ou augmentés pour les déchets.
  <br>
  Si une entité n'est pas présente dans la liste c'est qu'elle ne dispose pas de stock de l'unité choisie.
  <br>
  <br>
  Les besoins sont déterminés mensuellement comme décrit dans le <a href="systeme_eco.php">système économique</a> (en fonction du nombre citoyen des entreprises avec directeurs et des véhicules).
  Le responsable doit respecter les lois en vigueur dans son pays pour effectuer les affectations, cela n'est pas contrôler par EcoMicro.
  <br>

  <br>

  <b>Que signifie le 'Total contrôlé par l'étranger' dans le rapport économique d'une nation ?</b><br>
  Il s'agit de la somme des comptes étrangers en devise nationale.
  <br>
  <br>

  <b>Comment puis je créditer ou débiter un compte autre que le mien ?!!</b><br>
  C'est possible ??!! Oui, seul le responsable à la possiblité d'effectuer ces opérations.
  <br>

  Dans 'Etats'->'Listing Bancaire', cliquez le lien en vert 'Créditer un compte' ou 'Débiter un compte'.
  <br>
  Un mouvement bancaire est géréné, l'opération est donc visible avec un compte auxilaire dénommé 'Responsable'.
  Un message est aussi envoyé sur la ML éco de la nation.
  <br>
  <br>

  <b>Comment en tant que responsable puis je supprimer un compte ?</b><br>
  Dans 'Etats'->'Listing Bancaire', cliquez le lien en vert 'Supprimer un compte'.
  <br>
  S'il s'agit du dernier compte du titulaire ou d'un compte d'Etat utilisé au niveau du change, l'opération ne sera pas possible.
  <br>

