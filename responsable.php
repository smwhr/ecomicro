  <b><FONT size=4>Responsable Economique EcoMicro</FONT></b>
  </center>
  <br>  <br>
  <b><FONT size=4>Définition d'un Responsable Economique EcoMicro d'une nation virtuelle</FONT></b>  
  <br>  <br>  <br>
  - Un par pays  
  <br>  <br>
  - Il dispose d'autorisations spécifiques  
  <br>  <br>
  - Il dispose de la vision totale des informations du pays, notamment des comptes bancaires.  
  <br>  <br>
  - Il est le seul à pouvoir effectuer un virement depuis les comptes nationaux : non rattachés à une autre entité économique (citoyen, entreprise, organisme, ministère...) comme le compte bancaire national principal ou les comptes nationaux en devise étrangère.  
  <br>    
  > A ce titre, il en est donc responsable.  
  <br>  <br>  <br> <b>
      - Il est le seul avec le Contrôleur fiscal à pouvoir visualiser ces même comptes nationaux.
  </b>  <br>  <br>
  > Il ne s'agit pas d'un rôle EL, mais bien IL.   
  <br>
  > Le Responsable peut avoir un rôle au gouvernement (ministre...) ou être le représentant d'un pays (président...), mais cela n'est pas une nécessité.  
  <br>
  > Il est préférable que le Responsable économique et le Contrôleur fiscal soient deux personnes distinctes.  
  <br>
  > Les actions qu'il effectue doivent être définies de manière légitime IL. Il peut être lui-même le décideur légitime des actions ou simplement l'exécutant.  
  <br>   <br>  <br> 
  <b><FONT size=4>Les travaux du Responsable économique</FONT></b>  
  <br>  <br> 
  <b>- Il doit enregistrer chaque nouveau citoyen dans le système EcoMicro (depuis la liste des citoyens)</b>  
  <br>   
  > Une adresse mail valide est absolument nécessaire.  
  <br>  <br> 
  <b>- Il doit enregistrer chaque nouvelle entreprise dans le système EcoMicro (depuis la liste des entreprises)</b>  
  <br>
  > La capacité de l'entreprise doit être déterminée en rapport avec les capacités conseillées en fonction du type d'entreprise.  
  <br>
  > Un seul propriétaire peut être désigné (Il aura 100% des titres de la société).   
  <br>   > En cas de création d'entreprise avec plusieurs associés, un seul d'entre eux sera choisi pour la création et devra revendre les parts à ses associés en fonction de la répartition cible (achat à 1P§ remboursé par exemple). 
  Pour limiter les risques que le propriétaire unique temporaire ne veuille revendre ses parts, on peut choisir un autre citoyen (le Responsable Economique par exemple) mais cela doit être une règle définie au préalable.  
  <br>  <br> 
  <b>- Il peut modifier les données relatives à une entreprise.</b>  
  <br>
  > Notamment, il peut affecter le poste de Directeur (La liste proposée correspond aux citoyens actifs du pays.)  
  <br>  <br>  
  <b>- Il peut modifier les données relatives à son état</b>  
  <br>
  > Drapeau, Adresses du site, du forum/ML et de la ML economique. 
  <br>
  > Placer un citoyen du pays au poste de Responsable (mettre quelqu'un à sa place)  
  <br>
  > Placer un citoyen du pays au poste de Contrôleur fiscal. 
  <br>
  > Le compte bancaire national (utilisés pour les taxes et les opérations de changes) 
  <br>
  => Il doit être en devise nationale. 
  <br>  <br>  <br> 
  <b>- Il peut fixer le niveau des relations économiques.</b> 
  <br> 
  > Visible / Invisible :  Il est possible de rendre invisible un pays pour un autre. Cela afin de permettre à des pays très différents d'utiliser EcoMicro sans que cela ne soit perturbant pour les utilisateurs.  
  <br>
  > Economie Ouverte / Fermée : Permet de rendre possible (ou pas) les transactions économiques. Il s'agit d'une action éminemment de politique extérieure.  
  <br>  <br>  <br> 
  <b>- Il peut fixer le niveau des taxes :</b> 
  <br>   > Taxe interne : Pays1 = Pays2  
  <br>   > Taxe Externe : Pays1 <> Pays2. Il s'agit d'une action éminemment de politique extérieure.  
  <br>   > L'acheteur du Pays1 payera la taxe (en plus du prix de vente) qui sera versée sur le compte bancaire national.  
  <br>  <br>  <br> 
  <b>- Il peut fixer le niveau du taux de change.</b>  
  <br>   > Le taux est utilisé de manière automatique pour toute transaction entre deux devises (versement d'un montant d'une devise sur le compte d'une autre)  
  <br>   > Le compte bancaire est le compte national en devise étrangère correspondante (compte créé automatiquement à la création d'une nouvelle nation adhérante).  
  <br>      => Ce compte doit disposer des liquidités nécessaires pour assurer les échanges.  
  <br>      => Le Responsable Economique a donc le devoir d'avertir les autorités compétentes de ce niveau de liquidité afin que celles ci prennent les décisions nécessaires :  
  <br>         * C'est à dire d'alimenter ou pas le compte en devise.  
  <br>         * Alimentation qui peut être effectuée par le Responsable Economique si l'origine des fonds est un autre compte national. Le plus souvent se sera l'achat de devise étrangère (Opération qui consiste en deux versements croisés distincts et non liés informatiquement entre les deux pays, effectués par le Responsable Economique sur accord des autorités légitimes à partir des comptes nationaux)  
  <br>  <br>  <br> 
  <b>- Il peut visualiser le détail de l'intégralité des comptes bancaires nationaux.
  </b>  <br>   > Créditer un compte bancaire : cela crée de la monnaie  
  <br>   > Débiter un compte bancaire : cela détruit de la monnaie  
  <br>   > Supprimer un compte bancaire (détruit les fonds)  
  <br>   > Supprimer une transaction périodique  
  <br>  <br>    > La nation peut définir une règle pour la masse monétaire et définir de quel compte l'argent est détruit ou crée, pour l'ajuster (ex : 1 citoyen actif = 100 000 P§ à Prya) 
  <br>      => Le Responsable Economique n'aura qu'a suivre les règles définies.   
  <br>      => Pour des échanges économiques coéhérents il est nécessaire que les Etats appliquent des règles semblables.  
  <br>  <br>   > La nation peut vouloir rapatrier le solde des comptes bancaires non utilisés (suite au départ de citoyen par exemple). 
  <br>      => Pour se faire, le Responsable Economique n'aura d'autre choix que de débiter le compte en question et créditer le compte devant recevoir les fonds (Il n'existe pas d'outil de transfert de fonds). 
  <br>      => Pour les entreprises, l'opération doit s'effectuer par une transaction classique :  
  <br>         * Le Responsable Economique affectera temporairement le poste de Directeur de l'entreprise à un citoyen actif et volontaire (lui-même par exemple) et ce citoyen effectuera une transaction vers le compte destinataire.  
  <br>  <br>  <br> 
  <b>- Il doit tenir à jour la liste des Dettes des entreprises.</b> 
  <br>  <br> 
  <b>- Il doit veiller à la cohérence des prix des nouveaux produits créés par les entrepreneurs.</b> 
  <br>  <br> <b>- Pour rapatrier les titres (actions)</b>  <br>   
  > Il n'y a pas d'outil  
  <br>   > Pour une entreprise il faut affecter temporairement un Directeur et proposer le rachat de ces titres (à 1 P§ par exemple), le Directeur nouvellement nommé n'aura plus qu'à accepter.  
  <br>   > Pour un citoyen, il n'y a aucun moyen de le faire : Il faut me le demander pour que j'agisse directement dans les bases du système.  
  <br>  <br>  <br> <b>- Répartition / Distribution de l'argent public.</b>  <br>   > Le compte national principal reçoit les taxes sur les opérations de vente.  
  <br>   > Différents comptes nationaux peuvent être créés et utilisés : Impôts et Privatisations par exemples.  
  <br>   > L'utilisation de ces fonds doit être définie par l'autorité légitime.  
  <br>      => Autorité qui doit être tenue informée de la situation par le Responsable Economique (contrôle assuré par le Contrôleur Fiscal).  
  <br>      => Ainsi, il peut être défini que l'argent doit être regroupé pour ensuite être distribué sur différents comptes bancaires (par exemple ceux du gouvernement).  
  <br>  <br>  <br> <b>- Besoin des provinces</b>  
  <br>   > Au niveau du détail de chaque Etat, sont indiqués les besoins de chacune des provinces en unité de stock.  
  <br>   > Ces informations sont déterminées chaque mois et se cumulent d'un mois sur l'autre.  
  <br>   > Chaque province a le devoir de subvenir à ces besoins (déterminés en fonction des citoyens, entreprises et véhicules de la province) 
  <br>   > Les Gouverneurs (Directeurs des provinces) doivent acquérir ces ressources et les consommer (via le choix type de consommation). 
  <br>      => Le Responsable Economique peut toutefois, via le détail du pays, forcer la province ou même n'importe quelle entreprise possédant ce type de ressource, à consommer cette ressource afin de subvenir aux besoins de la population. (les unités de stock sont alors détruites et les besoins réduits d'autant).  
  <br>   > Les déchets sont gérés un peu différemment : 
  <br>      => Le Responsable doit chaque mois les affecter à la province correspondante (via le détail du pays). 
  <br>      => Le Gouverneur doit alors les faire retraiter en sollicitant une entreprise de retraitement.  
